package myfirstpackage;

public class Tables {

	public static void main(String[] args) {
		for (int i = 1; i <= 10; i++) {
	           
	           for (int j = 1; j <= 5; j++) {
	                System.out.print(j + " x " + i + " = " + (j * i)+ "\t" );
	            }
	            System.out.println(); 
	        }


	}

}
